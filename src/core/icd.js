export function lookupCountry(identifier) {
  const participantId = getParticipantValue(identifier);
  if (!participantId || !/^\d{4}:[0-9A-Za-z]+$/.test(participantId)) return null;
  return ICD_TO_COUNTRY[participantId.slice(0, 4)] ?? null;
}